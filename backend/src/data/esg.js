import prisma from '../config/database.js'

export async function getOverview() {
  // Calculate scores from actual data
  const carbonEntries = await prisma.carbonEntry.findMany()
  const policies = await prisma.policy.findMany()
  const programs = await prisma.cSRProgram.findMany()

  // Environmental score: % of entries on track
  const envOnTrack = carbonEntries.filter(e => e.status === 'On Track').length
  const envScore = carbonEntries.length > 0 ? Math.round((envOnTrack / carbonEntries.length) * 10 * 10) / 10 : 0

  // Social score: average completion rate of programs
  const socialAvg = programs.length > 0
    ? programs.reduce((acc, p) => acc + (p.targetHours > 0 ? p.currentHours / p.targetHours : 0), 0) / programs.length
    : 0
  const socialScore = Math.round(socialAvg * 10 * 10) / 10

  // Governance score: average policy progress
  const govAvg = policies.length > 0
    ? policies.reduce((acc, p) => acc + p.progress, 0) / policies.length
    : 0
  const govScore = Math.round(govAvg / 10 * 10) / 10

  const overall = Math.round(((envScore + socialScore + govScore) / 3) * 10) / 10

  // Emissions trend (aggregate by month - simplified)
  const totalEmissions = carbonEntries.reduce((sum, e) => sum + e.current, 0)
  const emissionsTrend = [
    { month: 'Jan', value: Math.round(totalEmissions * 0.22) },
    { month: 'Mar', value: Math.round(totalEmissions * 0.20) },
    { month: 'May', value: Math.round(totalEmissions * 0.18) },
    { month: 'Jul', value: Math.round(totalEmissions * 0.15) },
    { month: 'Sep', value: Math.round(totalEmissions * 0.13) },
    { month: 'Nov', value: Math.round(totalEmissions * 0.12) },
  ]

  // Recent activity from audit logs
  const recentLogs = await prisma.auditLog.findMany({
    take: 5,
    orderBy: { createdAt: 'desc' },
    include: { user: { select: { name: true } } },
  })

  const recentActivity = recentLogs.map((log, i) => ({
    id: i + 1,
    type: 'activity',
    text: `${log.user?.name || 'System'}: ${log.action}${log.details ? ' — ' + log.details : ''}`,
    module: 'System',
    when: formatRelativeTime(log.createdAt),
  }))

  // Fallback activity if no logs yet
  const activity = recentActivity.length > 0 ? recentActivity : [
    { id: 1, type: 'report', text: 'Quarterly Emissions Report generated by Sarah Jenkins.', module: 'Environmental', when: '2 hours ago' },
    { id: 2, type: 'milestone', text: 'Global Operations reached Zero-Waste Milestone.', module: 'Gamification', when: '5 hours ago' },
    { id: 3, type: 'alert', text: 'New Compliance Directive detected for EU Supply Chain.', module: 'Governance', when: 'Yesterday' },
  ]

  return {
    scores: { overall, environmental: envScore, social: socialScore, governance: govScore },
    emissionsTrend,
    recentActivity: activity,
  }
}

export async function getEnvironmental() {
  const entries = await prisma.carbonEntry.findMany({
    include: { department: { select: { name: true } } },
    orderBy: { createdAt: 'desc' },
  })

  const totalCO2e = entries.reduce((sum, e) => sum + e.current, 0)
  const scope2 = entries.filter(e => e.scope === 2).reduce((sum, e) => sum + e.current, 0)
  const scope3 = entries.filter(e => e.scope === 3).reduce((sum, e) => sum + e.current, 0)
  const totalTarget = entries.reduce((sum, e) => sum + e.target, 0)
  const sustainabilityIndex = totalTarget > 0 ? Math.round((1 - totalCO2e / totalTarget) * 100 + 50) : 50

  return {
    summary: {
      totalCO2e: Math.round(totalCO2e * 10) / 10,
      scope2: Math.round(scope2 * 10) / 10,
      scope3: Math.round(scope3 * 10) / 10,
      sustainabilityIndex: Math.min(100, Math.max(0, sustainabilityIndex)),
    },
    entries: entries.map(e => ({
      id: e.id,
      source: e.source,
      department: e.department.name,
      scope: e.scope,
      target: e.target,
      current: e.current,
      status: e.status,
    })),
  }
}

export async function getSocial() {
  const programs = await prisma.cSRProgram.findMany()
  const participations = await prisma.cSRParticipation.findMany({
    include: {
      user: { select: { name: true } },
      program: { select: { title: true } },
    },
    orderBy: { createdAt: 'desc' },
    take: 20,
  })

  const totalParticipants = await prisma.cSRParticipation.count()
  const totalHours = participations.reduce((sum, p) => sum + p.hours, 0)

  return {
    kpis: {
      csrActivities: programs.length,
      participants: totalParticipants,
      volunteerHours: programs.reduce((sum, p) => sum + p.currentHours, 0),
      trainingRate: 92, // Would come from HR integration
      diversityScore: 8.4,
      impactScore: 9.1,
    },
    participation: participations.map(p => ({
      id: p.id,
      employee: p.user.name,
      department: 'General', // Simplified
      activity: p.program.title,
      status: p.status,
      points: p.points,
    })),
  }
}

export async function getGovernance() {
  const policies = await prisma.policy.findMany({ orderBy: { updatedAt: 'desc' } })
  const audits = await prisma.audit.findMany({ orderBy: { scheduledAt: 'asc' } })

  const activePolicies = policies.filter(p => p.status === 'Active').length
  const pendingAck = policies.reduce((sum, p) => sum + (100 - p.progress), 0)
  const upcomingAudits = audits.filter(a => a.status === 'Scheduled').length

  return {
    kpis: { activePolicies, pendingAck, openIssues: 12, upcomingAudits },
    policies: policies.map(p => ({
      id: p.id,
      name: p.name,
      category: p.category,
      version: p.version,
      status: p.status,
      progress: p.progress,
    })),
  }
}

export async function getGamification() {
  const challenges = await prisma.challenge.findMany({ orderBy: { createdAt: 'desc' } })
  const leaderboard = await prisma.leaderboardEntry.findMany({ orderBy: { rank: 'asc' } })

  return {
    challenges: challenges.map(c => ({
      id: c.id,
      title: c.title,
      status: c.status,
      percent: c.percent,
    })),
    leaderboard: leaderboard.map(l => ({
      rank: l.rank,
      team: l.team,
      points: l.points,
    })),
  }
}

// Helper
function formatRelativeTime(date) {
  const diff = Date.now() - new Date(date).getTime()
  const hours = Math.floor(diff / (1000 * 60 * 60))
  if (hours < 1) return 'Just now'
  if (hours < 24) return `${hours} hour${hours > 1 ? 's' : ''} ago`
  const days = Math.floor(hours / 24)
  if (days === 1) return 'Yesterday'
  return `${days} days ago`
}
